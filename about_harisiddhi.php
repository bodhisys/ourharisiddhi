Next day the oldest person of the family goes to astrologist to make the birth record and he writes the record in Nepalese paper forecasting his good or bad luck. To make the free from the evils the god is worshipped mixing with beaten rice, soya bean, ginger. On the 4th day of birth, astrologist put certain name to the infant and called name is firstly called by maternal aunt.</p><p>
After 6 months if daughter or 7 months if son is feed rice-pudding. That day the god and goddess are worshipped, Hair-cutting. First long hair of the son is cut by maternal uncle and provide cap as a gift. If daughter, she is put far from the son and male for 11 days before first menstruation. She is hidden in certain dark room. If desired the people can took their daughter along with. The custom is that their relatives bring to feed roasted grains as maize, wheat, soya bean. On the 12th day early morning, they take bath and worship the morning sun. They stand in a row and go to worship the god.
