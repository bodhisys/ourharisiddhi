						<h3>Death culture<a href="#"></a></h3>
						<div class="acc-section">
							<div class="acc-content">
								<p>If the infant less than 4 months is died, their family members take it to bury at riverside in mid-night. No any tradition is followed for it.</p><p>
If the infant more that 4 months but before Brata Bandha or Gufa is died, member of Guthi (religion society) take on hand and reached to grieve-yard to burn. The custom is finished within 4 days.</p><p>
If the person after Brata Bandha and Gufa is died, their bodies are carried in a bamboo stair-case by the maternal sons or by the members of religion society to the cemetery for burn. After the complete burn of the body, his/her closer father, son, brother go to collect the ash and take to the Ganga (river side) to flow. If the sun is not set in the west. Otherwise, it follows in the next day morning when the sun rises in the east.</p><p>
4th day along of it, relatives and good-hearted persons come to condole the family. This type of death has to follow various social tradition but if a girl on the time of Gufa(hidden-room) is died, its body is not bring out but bring down straight from dead place making holes and buried on the ground floor. The tradition is not followed it also.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">4th day</strong> of death (Lochaa), relative females come to condole the family with beaten rice, vegetables and grains.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">7th day,</strong> it is believed the Newar society that the sprit or soul of the death person come to accept the foodings provided by his/her daughter/mother. So, various good foodings are offer outside the main gate.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">10th day,</strong> mourning family go to cut their hair and moustache (the son has to cut even the eye brow) and go to take bath. The oldest member (Thakali) provides the mourning dress to wear for the son.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">12th day,</strong> the Brahmin comes to pray for the family and purified the house.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">45th day,</strong> again Brahmin comes and pray and male members wear white caps.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">6 months,</strong> Brahmin comes to pray and relatives are invited to participate in worship and provide them food.</p><p>
<strong style="font-weight:bold; font-style:italic; color:#EEE;">1 year,</strong> gods are worshipped, relatives are called and mourning dress is unfastened and put on common dress. Thus, the death culture is ended.</p>

