						<h3>Wedding<a href="#"></a></h3>
						<div class="acc-section">
							<div class="acc-content">
								<p>In Newar society, the wedding is celebrated at the age of 20-25 years. The marriage is arranged by two middle-men by both sides. The tradition is that the group of people with odd numbers 9, 11, or more goes to the bride's house. Taking Sindur (red powder), daffodil flower, dress, ornaments and fooding.</p><p>
Bride is welcomed on the main gate as Laxmi (goddess of wealth) and next day, bride's family welcomes the bridegroom as Narayan (the god of truth) and takes nuts and gives him dress and some money. On the 4th day bride's side come to call the daughter and take her to their house.<p></p>
Nowadays legal marriage, love marriage is also found. If they love each other on the age, they ran far away from the family for some days and if the family accepted they become the couple.</p><p>
In the Hindu religion, sexual intercourse is not accepted before marriage. In the legal provision, the marriage is accepted if the age of male is not less than 18 and 16 for female and there should not be different the age more than 20 years between them Re-wedding. If divorced legally, they are free to re-marriage with other person.
</p>
