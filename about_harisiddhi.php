					  <div class="acc-section">
					    <div class="acc-content">
					  	 <p><strong style="font-weight:bold; font-style:italic; color:#EEE;">First day, a) Janai Purnima</strong> lies the 5th month of the year. First day people celebrate it. Scrubbing the house and house held materials, taking bath and wearing good clothes with sweet foods. On this occasion, Hindus change the thread which they are wearing for a year after incanting for it by Brahmins to protect by the evil spirit. Some people wear in the wrist which they unfasten and band to the cow's tail in the first festival of Tihar. i.e. Laxmi Puja believing that they could be success to cross the heaven ocean after his death. In this day people eat Kwati i.e. mixed of various grains and given it to the frog going to them rice planted field.</p><p>
Frogs are worshipped because there is a legend that a giant lived near the Kathmandu valley forest. He troubled the people by stealing the cattle and killing their children, so that people were afraid of him.</p><p>
One day a frog went closer and teased and scolds him again and again. He became very angry with that little creature and tried to catch it but a frog jumped one after another when he tried to catch it. At last, a frog went to the mud and a giant also went there. He dipped on the mud and could not get out of it. In this chance villagers thrown stones on him and killed the giant. So the frog is worshipped by the villagers.</p><p>
Lakhe, Giant mask dances are performed around the village with traditional music bands. The villagers who are happy to see the dance give some money to the dancer and collecting some grains and money they have dinner in the music house.?</p><p>
Second day, cow festival, in the memory of the death member, real cow or acting cows walk around the village as they are taking to graze. And milked on the way by actor of milkman. Some people make special type of rally with music singing the sorrowful words explaining the death and how much it hurted to the living members and wished to reach the heaven. Some people disguised as they liked to make laugh the hurted people so that they might be forgotten the grief.</p><p>
Cow festival is valuable to common people also. The law of the land provided freedom and the lose characters to satire, condemn against the government and higher officer for their bad service to the country.</p><p>
