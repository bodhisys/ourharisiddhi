There is a pond before the Harisiddhi temple. All of the musical band of Harisiddhi present near the temple and priests one by one come down from the temple wearing sacred clothes as wings and certain person collects that sacred clothes near the pond and swim with their wearing clothes one by one. At last, a person carrying the head of a buffalo swims and then common people are allowed to swim.
