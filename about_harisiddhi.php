								<p>This is the main part of the 12 year tradition. The potter is admitted and some plot of land is provided to make required Kalash guru priest determined a day to make the Kalash i.e. 7 to re-establish and 29 to throne Kalash bathing.</p><p>
When they are ready, the priests take fasting and bring them to the village. They are welcomed at the northern gate of the village by guru and they are taken to the Nasao Dyo temple.</p><p>
The guru burns the holy-wood and binds those 7 Kalash by chanted thread and remaining are surrounded to it, filling the water.
In the evening, the guru throne the 'Kalash bathing' to the priests according to their role in dancing. One after another they lift the Kalash and rounded 3 times to the fired point and poured the Kalash water on their bodies and then broke these.</p><p>
Three old Kalash established in Nasao Dyo are removed and thrown to three different places. Ganesh throw inside the temple, Bhairab throw to the pith temple and Kumari throw at the garden.</p><p>
Similarly, four Kalash established in Harisiddhi temple are also removed. Main goddess Maheswori throw one inside the temple and remaining three by senior priests to the pond nearby the temple. Then new 7 Kalash are established in both places i.e. 3 in Nasao Dyo and 4 in Harisiddhi temple. They are the symbol of god.</p><p>
The animals are sacrificed where the Kalash are thrown as follows:-</p>
<ul class="listStyle">
  <li>At Nasao Dyo temple, inside a goat and outside a buffalo.</li>
  <li>At pith a goat and a buffalo</li>
  <li>At the garden, a goat is sacrificed</li>
  <li>At the middle distance of Nasao Dyo and Harisiddhi temple, a buffalo and</li>
  <li>At Harisiddhi temple, inside a goat and outside 5 buffalo.</li>
</ul>
<p>They are sacrificed all at a time. People stand in a queue to inform one another to sacrifice and match the voice and sacrificed.
The next day all men in the village are invited to have feast and the following day all women are invited to have it.
After the completion of all these process, the mask dance trainee rehearsal without mask but cover their face by flowers so as not to be Ashame when done some mistake.</p><p>
Later the full phase of mask dance is performed contributing the expenditure required for dancing by the local government (VDC) and the people. The expenditure is more than one lakh (one hundred thousand).</p>


