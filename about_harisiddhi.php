					  <h3>Yeya Purnima<a href="#"></a></h3>
					  <div class="acc-section">
					    <div class="acc-content">
					  	 <p><strong style="font-weight:bold; font-style:italic; color:#EEE;">Yeya Purnima</strong> lies in the 6th months of the year. This festival is prolonged from Malla dynasty Pratap Malla noted in the history book. But it is believed that this festival is prevailing from many years ago.</p><p>
Once the mother of heaven king Indra needs some flowers to worship. To fulfill mother's requirement Indra himself came down to the earth and plucked the flowers in a garden. The people caught as a thief and bind him. Many people from neighbor villages came to see the thief and some people said to rescue him knowing the king and rescued him that he is the god of rain. Since then the custom to bind a thief on a wooden pole is prevailed. If the Indra is happy, there will be enough rain for crops. So, he is worshiped on this festival. This festival proceeds in three stage.</p>
<ul>
  <li>a) Distribution of snacks</li>
  <li>b) Worship the Indra and </li>
  <li>c) Horse dance</li>
</ul>
<br />
<p><strong style="font-weight:bold; font-style:italic; color:#EEE;">A. Distribution of snacks :</strong><br />
A kind of snacks with beaten rice, green vegetables, soya bean, ginger, pieces of meat, etc are distributed by the villagers each other in the memory of their past families, local wine and rice-beer also served.</p>

<p><strong style="font-weight:bold; font-style:italic; color:#EEE;">B. Worship the Indra :</strong><br />
In the evening all musical band of the village gather outside the southern gate. The youth (male, female) with modern dresses to attract each other, who are on the stage of marriage liked to show their youth appearance appeared in the function. Three main priests of Harisiddhi goddess lead the function in a queue. Females appear in new fashion with various ornaments to make good look. This is the chance to do so. Behind a queue, music band follow with local attractive songs. The function continues up to midnight with local attractive songs. The function continues up to midnight.</p><p>
The local government VDC family welcomes the participants at the southern gate. The jatra ended in front of Harisiddhi temple. The participated music bands are offered some tips by VDC. There are 18 traditional music bands. Among them 8 are of different musical band which produce 8 different music plays.</p>

<p><strong style="font-weight:bold; font-style:italic; color:#EEE;">C. Horse dance :</strong><br />
After the completion of the jatra (procession) certain music group brings horse dance acting kings and queens of historical Nepal. Small boys and girls act on it. So, it is very interesting. They sing the old song describing the situation how the Harisiddhi is established, when the king managed the dance and why?</p>
