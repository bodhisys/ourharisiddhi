Pharping Champadevi hiking is an easy and enjoyable day hike in the southern hill of Kathmandu valley. This hiking tour starts in the morning after an hour drive to Pharping village on the way to celebrated Dakshinkali Temple. Dakshinkali Temple is a symbol of goddess Kali and attracts thousands of devotees every day. There is special worship take place every Tuesday and Saturday where hundreds of animals scarified on the name of god to fulfill the human wish.

It will take about 2 hours to reach on top of ridge. Champadevi hill (2285m.) is also a pilgrimage site itself having Buddhist Stupa and Hindu Shrine on the top. Walking through the lush pine forest on well develop trail is really wonderful experience. Superb view of Langtang, Gauri Shankar, Jugal, Dorge Lakpa Himalayas are the major attraction of the hiking. Apart from it, we can observe the bustling Kathmandu city and spectacular village settlements from the top. Several return routes are possible.
