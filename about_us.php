        <h4>The journey till today was a journey full of "moments to be remembered". The website development of <a href="http://www.ourharisiddhi.com">ourharisiddhi.com</a> was started with a simple project. The project was about the findings of the past scenarios of the establishment, present situations, culture variation among the localities, the methodologies of way of life of people ("<strong>jalmis</strong>") of this village, Harsiddhi by <a href="http://www.subodhmaharjan.com.np" target="_blank">Mr. Subodh Maharjan</a>. Later on, after the completion of the project an idea hit the minds of three of the current our Harsiddhi team members: <a href="http://www.subodhmaharjan.com.np" target="_blank">Mr. Subodh Maharjan</a> (The Creator and Manager of Our Harsiddhi page himself), <strong>Mr. Jiten Maharjan</strong> (Multimedia Coordinator of The Team) and <strong>Mr. Subash Maharjan</strong> (...). The idea was to flow the information collected about the village to the local people living inside and outside the village, also to the ones living outside the nation. This desire to unfold the research they had done on their birth place lead them to seek the possible methods to do it, and in today's globalizing world, internet was the best thing that these two young enthusiastic men found sensible. With their strong will to create something, they first started a simple website <a href="http://www.ourharisiddhi.com">ourHarsiddhi.com</a>, along with a page "<a href="http://www.ourharisiddhi.com/about_harisiddhi.php">our Harsiddhi</a>" in facebook.<br /><br />

The facebook page got many likes and did bring up many members of <a href="http://www.ourharisiddhi.com/about_harisiddhi.php">Harsiddhi</a> to participate. The continuous praise for their work led them to want to do better on the website. But creating a website and managing it for a long better time wasn't an easy job. Lack of efficient manpower and insufficient amount of budget created few obstacles to actually publish the page locally. But, still no hope was lost. Later on, new project was conducted. The project named "photoshoot' click'o graphy" for our Harsiddhi page. During the project few other interested pupils joined the team, thus the second project got a big success. After the project, a serious workout for the website was started. And hence we're here with the full website launched. <br />
We now are the group of Eight led by <a href="http://www.subodhmaharjan.com.np" target="_blank">Mr. Subodh Maharjan</a> to create a better page for people of Harsiddhi. We are, <a href="http://www.subodhmaharjan.com.np" target="_blank">Subodh</a> (the webpage designer and the team coordinator), <strong>Jiten</strong> (the multimedia editor), <a href="http://www.subashmaharjan.com" target="_blank">Subash</a> (developer), <strong>Arun</strong> (photographer), <strong>Prajwol</strong> (web manager), <strong>Janam</strong> (Editor), <strong>Sumeet</strong> (Graphic Designer), <strong>Rown</strong> (assistant photographer).
<br /><br />
The context of this site are designed in such of way that it covers the past, present and even future of the village. This site has the information related to <a href="http://www.ourharisiddhi.com/about_harisiddhi.php">Harsiddhi</a>, starting from its creation to the daily events conducted today. Through this page "<strong>jalmis</strong>" (people of harissidhi) as well as the others will get a chance to peek in lives of people of <a href="http://www.ourharisiddhi.com/about_harisiddhi.php">Harsiddhi</a>. Also will be updated with the events held as well will be informed about the events to take place in the village in the near future. Through this, we plan to also notify the people who are staying abroad with information on how and what the village is "happenning". Plus, few pages are dedicated to the "<strong>jalmis</strong>" to expose the hidden talents of and from the village. A donation for the development of village, for the poors, as well as for the website build up is also appreciated.
And finally this website will play a vital role in keeping the "<strong>jalmis</strong>" together forever. 
<br /><br />
<strong>"Together we stand, divided we fall."</strong>, and "<strong>jalmis</strong>" are well known among other villages as the one's who stand together. This site will play a vital role to unite the people technologically. So, lets get started ....
