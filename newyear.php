        <h1>Nhu Dan / Nepal Sambat / New Year</h1>
        <p>Our Harisiddhi - Nhu Dan, Nepal Sambat, New year</p>
        <h4>
<strong>"Vintuna du Vintuna !! Nhu: danya Vintuna"</strong>, a slogan one would hear being yelled during nhu dan according to Nepal sambat around the newar localities. 'Nhu' meaning new and 'dan' meaning year, nhu dan is the New Year day of newa people. For newars, this day signifies "the new beginning", the day that all newars believe will bring luck, fortune and better life. This day usually falls on the 4th of Tihar, the day after Laxmi puja. People around as well as outside the valley celebrate this day by greeting one another, participating in various processions, cultural programs and finally by performing mha: puja.<br />
As is falls, during the "festival of lights", tihar, quite a lot of festivity makes it a celebration.
<br /><br />
Nhu Dan in Harisiddhi is celebrated no differently. On this day, certain group people of certain area are given responsibility to organize a small procession. Almost every individual of the village participate in the rally conducted. Various groups, dress up in there own mannered dresses. Those groups would include a group of friends, a bhajan/musical groups, family groups, communities or so else.
The rally commences on the daybreak from the main temple area, Lachi, and after taking a one round tour of the village, end in the same place. The music related groups participating in this rally come prepared with their musical instruments and perform during the tour. Some of them would also have their cultural shows, dances, parades and so on. Lakhe pyakha:, Doko pyakha:, Salcha pyakha: etc are some of them that are seen on the streets of Harisiddhi during this day. The parade finally ends in lache: where all the participants are served with samae. Later, everybody return back to their homes to prepare for mha: puja.
<br /><br />
Newars have been known to celebrate any festival or auspicious occasion in the majestic manners. The rituals of mha: puja usually being as the darkness of night sets in just after evening. And then in the middle of glowing diyos lit, the Mha Puja begins.
<br /><br />
First of all, the mandaps are drawn on the ground for every individual member of the family. The mandap is a complex work of art designed with white, red and yellow colored powder. Rice, wheat, marigold flowers incense sticks and camphor are laid around it.
Then after the eldest member of the house takes the seat, the ritual begins.
It starts with the eldest female member of the house lighting the sacred lamp as a tribute to the gods. Each family member begins the process of worshipping and cleansing their own bodies and minds by lighting the ita (made of cotton cloth dipped in oil), continued with the worship of their body, starting from their shoulders, head and even gods for giving the body, life. The ritual ends with the shagun – each consisting of a full-boiled (and then fried) egg, dried fish, a piece of meat, ginger and who(bara).
.<br /><br />
 The itas burnt during this ritual is a metaphor of life. And this day it glows the brightest, as in this time of the year, all the family members sit around each other in the dim, burning light of the itas, sharing the smiles and wishing to be together through  the newer year too. Nhu Dan is that time of the year when Newars reflect on what they have achieved in the year gone by. This day brings new hope for a better life.
