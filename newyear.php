<strong style="text-decoration:underline;">Few facts about Nepal Sambat:-</strong><br />
Nepal Sambat (नेपाल सम्बत) is the national lunar calendar of Nepal. It was used throughout Medieval Nepal, and into early modern Nepal, until year 1960BS. It was started in 880 AD during the reign of King Raghav Dev to commemorate the payment of all the debts of Nepalese people by a person named Sankhadhar Sakhwa. The legend has it that he raised the funds through alchemy, turning the sand of the Bagmati River into gold dust. Nepal Sambat has its own special relevance for Newars living in Nepal. Nepal Sambat is one among a few calendars native to Nepal.<br /><br />
Nepal Samvat, a lunar calendar, is an alternative of "saka sambat" a Hindu calendar with main difference being, Nepal Sambat era lags behind the saka sambat by 802 years. This calendar came into official use during the reign of king Raghabdev, immediately after the completion of the Saka Samvat 802 (on 20 October 879 AD). The year 804 was approaching within a year and according to legend, his decision was guided by his fear of number 804 that some people still believe, brings misfortune. People with traditional belief still try to escape with number 8 that comes together with 12 (in Nepali -ath barha). Doing math correctly, 804 adds up to 12 and 804 means 8 along with 12.<br /><br />
Nepal Samvat is a unique calendar since Nepal Samvat is the only calendar which is named after a country while all other calendars are named after some rulers or religious leaders. This calendar is said to have been introduced by a common person Shankhadhar Sakhwa. This calendar was in continuous official use Nepal (and not just in Kathmandu valley as is widely thought) and had significant influence, to be mentioned in the documents of Tibet, China, and kingdoms of northern India. After the unification of Nepal, under the Shah rulers, the calendar was in use as is evident by Sugauli Treaty. In 1903 AD, Rana prime minister Chandra Shamsher replaced Nepal Samvat with the Bikram Samvat, which is in use as the official calendar in Nepal to this date. Since the founder of the Nepal Samvat, Sankhadhar Sakhwa has been recognized as a national hero of Nepal. Nepalese people especially, inside Kathmandu Valley,are also demanding to preserve as well as reintroduce the Nepal Samvat as Nepal's official calendar. The new year is celebrated every year in the religious Tihar festival, a day after Laxmi puja. 
