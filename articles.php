<h2>About Newa people</h2><br />
The Newa (Nepal Bhasa: नेवाः Newā(h), Classical Nepal Bhasa: नेवार Newār or नेवाल Newāl) (Nepali: नेवार जाति listen (help•info)) are theindigenous people and the creators of the historical civilization of Nepal's Kathmandu Valley. The valley and surrounding territory have been known from ancient times as Nepal Mandala, its limits ever changing through history.<br /><br />
Newas have lived in the Kathmandu Valley since prehistoric times, and immigrants that arrived at different periods in its history eventually merged with the local population by adopting their language and customs. Newars are a linguistic and cultural community of mostlyTibeto-Burman and some Indo-Aryan ethnicities. They are bound together by a common language and culture. Their common language is Nepal Bhasa ("Newari" according to Statistics Nepal) or the linguistic progenitor of that language. Scholars have also described the Newars as being a nation.<br /><br />
According to Nepal's 2001 census, the 1,245,232 Newars in the country are the nation's sixth largest ethnic group, representing 5.48% of the population. In 2001, there were approximately 825,000 native speakers of Nepal Bhasa. Many Newar communities within Nepal also speak their own dialects of Nepal Bhasa, such as the Dolakha Newar Language. Nepal Bhasa is of Tibeto-Burman origin but has been heavily influenced by Indo-Aryan languages like Sanskrit, Pali, Bengali and Maithili.
