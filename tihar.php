        <h4>
Tihar, "the festival of lights", the second biggest festival of the nation is a five day event which occurs usually during the month of Kartik. Newar people have a different name for this festival. They call it "shwonti nakha:" This year (year 2068), Tihar starts on 7th and ends on 11th of Kartik. As per the theme of this festive, during these days all the houses around the country are well cleaned and during the nights diyo's are lit around the premises to fill the pleasant environment with the bliss of light and warmth.<br /><br />
<strong>The first day</strong> of tihar is known as '<strong>Kag tihar</strong>', this day belongs to crows. Crow is considered as an underworld messenger. On this day crows are worshiped with tika's, garlands and the offerings of food placed on a plate made out of leaves in the morning before anyone in the house takes in food. According to ancient myth, crows happen to drink the water of life, because of which, crows would never die. Thus you can see crows everywhere sitting without the fear of human beings. Crow the messenger of death is worshiped on the first day of tihar. In harisiddhi, not much of the people celebrate this event though.<br /><br />
<strong>The second day</strong> is called '<strong>Kukur tihar</strong>', also known as "kicha puja" according to newars. This is the day when dogs, the steed of the fearful Bhairab, the god of destruction, worshiped.  The legend says that there is a dog at yama's gate guarding the gate to the underworld. Thus, the dogs are worshiped with big red tika drawn on their forehead, beautiful garland around their neck and offerings of a delicious meal on this day. We pray to the dog to guard our houses and to divert destruction away from our homes. On this day you can see dogs running around with garlands on their neck. In Harsiddhi, people owing the dogs, the ones who have dogs as pets worship their dogs on this day. Also, few people worship the local dogs found on the streets.<br /><br />
For this festive, <strong>the third day</strong> is considered the most important day. It is the day for '<strong>Laxmi puja</strong>', the day when Goddess of wealth, Laxmi is worshiped. Early in the morning, local people worship cows. As the other prior worships, tika is put on cows' head and a garland around the neck, then they're fed with delicious food. A cow is the most holy animal for Hindus. Cow is the national animal of Nepal too. Then, in the evening goddess laxmi is worshipped. Before this event, the houses are cleansed and decorated. The entire house is decorated with lit oil lamps in every doors and windows. A small potion of the house out side the main door is painted with red mud and an oil lamp is lit on it. A pathway is made from there to the place where the Laxmi puja is to take place. As the myth has it that, goddess Laxmi during laxmi puja take a tour around the world to see how well the puja is taking place. Then she lands on the house which is nicely decorated and is well too clean and leave the place full of wealth. Laxmi, goddess of wealth is worshipped performing the traditional rituals. On this day throughout the evening groups of girls go around neighbor houses singing songs, and performing dances. This play or an act is termed as "bhailo khelnu". They sing "bhailini" songs. They are taken as guests and given gifts. This day the entire place is lively through out the night. People of harisiddhi also worship goddess laxmi durning the evening hour. Many young girls too go around different houses playing "bhaili".<br /><br />
<strong>The fourth day</strong> of the festival is the most important day of the whole year for newars. Normally most of the people perform '<strong>Goru puja</strong>', ox worshipping. The ox is worshipped with tika, garland and then a delicious meal is fed to it. On the other hand people who follow lord Krishna perform '<strong>Gobhardan puja</strong>'. These people build a small hill made out of cowdung and put some grass on it then worship it. This puja symbolises the act of lord Krishna when he lifted the gobhardan hill and saved millions of people and cows from floodwater.<br /><br />
On this very day Newar community that include people of Harsiddhi celebrate their New Year day, (<strong>Nhu dan:</strong>). The <strong>New Year day</strong> according to Nepal Sambat lay on this day. Nepal Sambat (नेपाल सम्बत) is the national lunar calendar of Nepal. It was used throughout Medieval Nepal, and into early modern Nepal, until year 1960BS. It was started in 880 AD during the reign of King Raghav Dev to commemorate the payment of all the debts of Nepalese people by a person named Sankhadhar Sakhwa. The legend has it that he raised the funds through alchemy, turning the sand of the Bagmati River into gold dust. Nepal Sambat has its own special relevance for Newars living in Nepal. Nepal Sambat is one among a few calendars native to Nepal. For newars, this day signifies "the new beginning", the day that all newars believe will bring luck, fortune and better life. People around as well as outside the valley celebrate this day by greeting one another, participating in various processions, cultural programs and finally by performing mha: puja.<br /><br />
Later in the evening hour of this day newar people perform '<strong>Mha puja</strong>' which literally means worshipping yourself. Mha: meaning the 'body' and puja meaning 'to worship', thus, Mha puja, the worship of the inner own self, is unique to Newa people. Newars believe that the body is the disguise of their soul. The body provides a space where the soul can dwell. Thus, newars worship their own body, thanking it for giving their life an opportunity to exist in this earth. We believe that one needs to understand and respect oneself before he/she can understand others. Mha puja is the method where purification, strengthening and understanding of one self is done. It also is distinct from other Hindu or Buddhist worships in that it is the worship of own self rather than usual worship of Gods and Goddesses or others. No other Newar festival is solely devoted to enriching oneself both physically and spiritually. <br /><br />
<strong>The last day</strong> of tihar is dedicated to brothers and sisters. This is the day of '<strong>Bhai tika</strong>', the day when sisters worship their brothers and exchange gifts and love. It is known as "<strong>Kija Puja</strong>" according to newars. The main theme behind bhai tika is the sisters praying for their brother's long life from Yama Raj, god of the underworld. On this day, in Harsiddhi usually elder sisters worship their brothers. Brothers sit on a floor while sisters perform their puja. Puja involves following a traditional ritual in which sisters circle brothers three times dripping oil on the floor from a copper pitcher. Afterwards, sisters put oil in brother's ears and hairs, and then give Tika. Also breaking of walnuts by sisters prior to giving Tika to brothers is also a common practice. tika, flower garland is put around brother's neck. Then brothers give tika to sisters in the same fashion. Sisters also receive flower garland around their neck. Brothers give gifts such as clothes or money to sisters while sisters give a special gift known as Sagun. Nowadays in harisiddhi, even the younger sister worship their brothers. Those without a sister or brother, join relatives or friends for tika. Sisters pray for their brother's long life to the Hindu God of Death (Yam Raj).<br /><br />
 The most exotic and dazzling festival come to an end after these five wonderful days of worships and offerings to the goddess laxmi and the underworld kingdom.
 
